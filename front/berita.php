                    <p style="text-align: justify;">
                        Resistansi antimikrob (Antimicrobial resistance atau AMR) merupakan suatu keadaan dimana mikrob mampu untuk bertahan pada dosis terapi senyawa antimikroba, terutama antibiotik. AMR menjadi ancaman serius bagi kesehatan masyarakat. Beberapa organisasi dunia sudah melakukan kajian terhadap dampak AMR. Salah satunya adalah Uni Eropa, yang menyatakan bahwa diprediksi 25.000 pasien meninggal tiap tahunnya di EU karena infeksi bakteri yang resisten, pada level global diprediksi 700.000 orang/tahun meninggal karena infeksi bakteri yang resisten. Diperkirakan terdapat 10 juta kematian/tahun antara tahun 2015-2050, dengan angka rata-rata resistansi yang meningkat 40%, dan jumlah kejadian terbesar di Afrika dan Asia, masing-masing sebesar 4,1 dan 4,7 juta jiwa. Pada tahun 2016, World bank juga melakukan kajian terhadap dampak ekonomi dari AMR secara global. Hasil kajian tersebut dipublikasi dalam laporan yang berjudul “Drug Resistant Infections: A Threat to Our Economic Future”. Berdasarkan kajian tersebut, Worldbank memprediksi, jika tidak dilakukan mitigasi segera, maka AMR akan mengakibatkan jumlah penduduk jatuh miskin akibat AMR diperikakan sebesar 28 juta orang, hasil peternakan global turun hingga 7,5%, biaya layanan kesehatan global meningkat hingga USD 1 milyar, jumlah ekspor global menurun hingga 3,8%. Sehingga perlu dilakukan upaya untuk menurunkan risiko AMR untuk menghindari potensi dampak yang akan datang.
                        <br><br>
                        Di Indonesia, telah terdapat Peraturan Menko PMK No. 7 Tahun 2021 tentang Rencana Aksi Nasional 2024-2027 terkait Pengendalian Resistansi Antimikrob menjadi dasar untuk pelaksanaan pengendalian resistansi antimikrob. Pasal 3 pada peraturan tersebut menyatakan bahwa:
                        Rencana Aksi Nasional (RAN) Pengendalian Resistensi Antimikroba merupakan pedoman bagi kementerian, lembaga, pemerintah daerah, dan mitra kerja dalam perencanaan, penyusunan, pelaksanaan, pemantauan dan evaluasi kebijakan pengendalian resistensi antimikroba.
                        <br><br>
                        RAN Pengendalian Resistensi Antimikroba bertujuan untuk meminimalkan muncul dan menyebarnya mikroba resisten, memastikan ketersediaan antimikroba yang aman, efektif, bermutu, dan terjangkau, serta penggunaan antimikroba secara bijak dan bertanggung jawab.
                        <br><br>
                        Pengendalian resistensi antimikroba yang meliputi aspek kesehatan masyarakat, kesehatan hewan (termasuk ikan), produk yang dikonsumsi masyarakat, dan kesehatan lingkungan melalui pendekatan One Health. One Health didefinisikan oleh organisasi dunia (FAO, OIE, WHO dan UNEP) sebagai pendekatan terpadu dan bersama yang bertujuan untuk menyeimbangkan dan mengoptimalkan kesehatan manusia, hewan, dan ekosistem secara berkelanjutan.
