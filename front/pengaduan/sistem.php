									<ul>
										<li style="text-align: justify;">Apabila tidak terbukti benar, maka Kepala Badan POM/Pejabat yang
											berwenang menyampaikan kepada Whistleblower dan mengembalikan nama baik Terlapor</li>
										<li style="text-align: justify;">Apabila terbukti benar, maka Kepala Badan POM/Pejabat yang
											berwenang menyampaikan surat ucapan terima kasih kepada Whistleblower dan mengambil tindakan
											terhadap Terlapor sesuai ketentuan</li>
										<li style="text-align: justify;">Kepala Badan POM dan Pejabat yang berwenang berkewajiban menjaga
											identitas Whistleblower selama pengaduan masih dalam proses penanganan dan hanya dapat
											mengungkapkan identitas pelapor untuk keperluan penyidikan dan persidangan</li>
										<li style="text-align: justify;">Hasil diterima Pelapor dan status selesai</li>
