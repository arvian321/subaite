									<p style="text-align: justify;">Untuk mempermudah dan mempercepat proses tindak lanjut, pelaporan
										disertai informasi sekurang-kurangnya:</p>
									<ol style="text-align: justify;">
										<li>Identitas pelapor yang jelas
											<ul>
												<li>Nama</li>
												<li>Alamat</li>
												<li>Nomor HP/email</li>
												<li>Profesi</li>
												<li>Asal pemohon (sebutkan nama perusahaan/instansi/masyarakat/lainnya)</li>
											</ul>
										</li>
										<li>Deskripsi Pelanggaran
											<ul>
												<li>Uraian pelanggaran yang dilakukan</li>
												<li>Terlapor serta pihak lain yang terlibat (bila ada) dan unitnya</li>
												<li>Tempat dan waktu kejadian</li>
												<li>Dokumen pendukung dan bukti lainnya (bila ada)</li>
											</ul>
										</li>
									</ol>
									<p style="text-align: justify;">Kami akan melindungi kerahasiaan identitas pelapor yang beritikad
										baik, laporan, maupun segala data lain yang terkait dengan laporan yang masuk melalui WBS.</p>
									<p style="text-align: center;"><strong><span style="font-size: 18pt;">Alur penanganan WBS di Badan POM
