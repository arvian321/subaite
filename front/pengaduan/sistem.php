									<p style="text-align: justify;"><em>Whistleblowing system</em> (WBS) adalah sistem untuk memproses
										pengaduan/pemberian informasi yang disampaikan baik secara langsung maupun tidak langsung
										sehubungan dengan adanya perbuatan yang melanggar perundang-undangan, peraturan/standar, kode
										etik, kepentingan umum, serta Korupsi, Kolusi, dan&nbsp;Nepotisme&nbsp;(KKN) yang terjadi di
										lingkungan Badan POM.</p>
									<p style="text-align: justify;">Pengelolaan WBS dilaksanakan berdasarkan Peraturan Kepala Badan
										Pengawas Obat dan Makanan Republik Indonesia Nomor HK.03.1.23.12.11.10050 Tahun 2011 tentang Tata
										Cara Pengelolaan dan Tindak Lanjut Pelaporan Pelanggaran (<em>Whistleblowing</em>) di Lingkungan
										Badan Pengawas Obat dan Makanan.</p>
									<p>Pelapor dapat membuat laporan pelanggaran melalui sarana/media sebagai berikut:</p>
