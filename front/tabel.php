  // Fungsi untuk filter pada input pencarian
  $(document).ready(function(){
    $("#searchInput").on("keyup", function() {
      var value = $(this).val().toLowerCase();
      $("#tableBody tr").filter(function() {
        $(this).toggle($(this).text().toLowerCase().indexOf(value) > -1)
      });
    });
  });

  $(document).ready(function(){
    $('#filterDropdown').select2({
      placeholder: "Pilih Filter",
      allowClear: true // Opsional: Menambahkan tombol untuk menghapus pilihan
    });

    // Event saat nilai dropdown berubah
    $('#filterDropdown').on('change', function(){
      var selectedValue = $(this).val().trim();

      // Lakukan filter tabel berdasarkan nilai yang dipilih dari dropdown
      filterTable(selectedValue);
    });

    $("#searchInput").on("keyup", function() {
      var value = $(this).val().toLowerCase();
      $("#tableBody tr").filter(function() {
        $(this).toggle($(this).text().toLowerCase().indexOf(value) > -1)
      });
    });

    // Fungsi untuk memfilter tabel berdasarkan nilai dropdown
    function filterTable(filterValue) {
      // Tampilkan semua baris tabel terlebih dahulu
      $("#tableBody tr").show();

      // Jika nilai filter dipilih, terapkan filter ke tabel
      if (filterValue !== "") {
        $("#tableBody tr").each(function() {
          var rowText = $(this).text().toLowerCase();
          // Sesuaikan kondisi filter dengan data tabel yang ingin difilter
          if (rowText.indexOf(filterValue.toLowerCase()) === -1) {
            $(this).hide(); // Sembunyikan baris yang tidak sesuai dengan nilai filter
          }
        });
      }
    }
  });
  </script>
  

